#include <iostream>
using namespace std;

int main() {
    int m = 45, n = 75;
    cout << "m = " << m << ", n = " << n << "\n";
    ++m;
    --n;
    cout << "m = " << m << ", n = " << n << "\n";
    m++;
    n--;
    cout << "m = " << m << ", n = " << n << "\n";
    return 0;
}
